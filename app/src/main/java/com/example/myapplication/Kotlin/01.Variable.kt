package com.example.myapplication.Kotlin

// 변수
// -> 상자
//      - 내 마음대로 원하는 것을 넣을 수 있는 상자 -> Variable
//      - 한번 넣으면 바꿀수 없는 상자 -> Value

// 변수 선언하는 방법
// Variable/Value 변수명 = 값
// var/val 변수명 = 값

var num = 10
var hello = "hello"
var point = 3.4

val fix = 20

fun main(args: Array<String>) {

    println(num)
    println(hello)
    println(point)

    // 엄청 긴코드

    num = 20
    hello = "Good Bye"
    point = 5.6

    println(num)
    println(hello)
    println(point)

//    fix = 100


}





